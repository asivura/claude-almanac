#!/usr/bin/env node
// Generate site/public/feed.xml — an Atom 1.0 feed of the 30 most recently
// updated docs. Runs as a prebuild step AFTER last-updated.mjs so it can
// read the cached date map from src/data/last-updated.json.

import { readdir, readFile, mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const siteRoot = path.resolve(__dirname, '..');
const repoRoot = path.resolve(siteRoot, '..');
const outFile = path.join(siteRoot, 'public', 'feed.xml');

const SITE_URL = 'https://claude-almanac.sivura.com';
const CONTENT_DIRS = ['features', 'guides', 'case-studies'];
const MAX_ENTRIES = 30;

function escapeXml(str) {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

function parseFrontmatter(text) {
  const match = text.match(/^---\s*\n([\s\S]*?)\n---/);
  if (!match) return {};
  const block = match[1];
  const result = {};
  for (const line of block.split('\n')) {
    const m = line.match(/^(\w+):\s*(.+)$/);
    if (m) {
      result[m[1]] = m[2].trim().replace(/^['"]|['"]$/g, '');
    }
  }
  return result;
}

async function listMarkdown(dir) {
  try {
    const entries = await readdir(dir);
    return entries.filter(
      (name) => name.endsWith('.md') && name !== 'README.md',
    );
  } catch {
    return [];
  }
}

async function main() {
  // Read cached dates from last-updated.json (generated by last-updated.mjs).
  let dates = {};
  try {
    const raw = await readFile(
      path.join(siteRoot, 'src', 'data', 'last-updated.json'),
      'utf8',
    );
    dates = JSON.parse(raw);
  } catch {
    console.warn('[feed] last-updated.json not found; dates will be empty');
  }

  const entries = [];

  for (const dir of CONTENT_DIRS) {
    const absDir = path.join(repoRoot, dir);
    const files = await listMarkdown(absDir);
    for (const file of files) {
      const slug = file.replace(/\.md$/, '');
      const content = await readFile(path.join(absDir, file), 'utf8');
      const fm = parseFrontmatter(content);
      const title = fm.title || slug;
      const description = fm.description || '';
      const updated = dates[slug] || null;
      if (updated) {
        entries.push({ slug, title, description, updated });
      }
    }
  }

  // Sort newest first, take top N.
  entries.sort((a, b) => b.updated.localeCompare(a.updated));
  const top = entries.slice(0, MAX_ENTRIES);

  const feedUpdated = top.length > 0 ? top[0].updated : new Date().toISOString();

  const xml = [
    '<?xml version="1.0" encoding="UTF-8"?>',
    '<feed xmlns="http://www.w3.org/2005/Atom">',
    `  <title>Claude Almanac</title>`,
    `  <link href="${SITE_URL}/"/>`,
    `  <link rel="self" href="${SITE_URL}/feed.xml"/>`,
    `  <id>${SITE_URL}/</id>`,
    `  <updated>${escapeXml(feedUpdated)}</updated>`,
  ];

  for (const entry of top) {
    xml.push('  <entry>');
    xml.push(`    <title>${escapeXml(entry.title)}</title>`);
    xml.push(`    <link href="${SITE_URL}/docs/${escapeXml(entry.slug)}"/>`);
    xml.push(`    <id>${SITE_URL}/docs/${escapeXml(entry.slug)}</id>`);
    xml.push(`    <updated>${escapeXml(entry.updated)}</updated>`);
    if (entry.description) {
      xml.push(`    <summary>${escapeXml(entry.description)}</summary>`);
    }
    xml.push('  </entry>');
  }

  xml.push('</feed>');
  xml.push('');

  await mkdir(path.dirname(outFile), { recursive: true });
  await writeFile(outFile, xml.join('\n'), 'utf8');
  console.log(
    `[feed] wrote ${top.length} entries → ${path.relative(siteRoot, outFile)}`,
  );
}

main().catch((err) => {
  console.error('[feed] failed:', err);
  process.exit(1);
});
